import pandas as pd
import re

# =============================================================================
# 【設計方針】
# 「file名ごとに変換テーブルが異なる」という仕様が核心。
# file1のS02→S名1、file3のS02→S名2 のように、同じコードでもfileによって
# 変換先が変わるため、「まずfileで絞る→その中でコード置換」の2段構えにしている。
# =============================================================================

# --- データ定義 ---
logic_df = pd.DataFrame({
    'file名': ['file1', 'file2', 'file3'],
    'ロジック': ['C01>0.5,S02<=0.5', 'C02>0.5,S03<=0.5', 'C01>0.5,S02<=0.5']
})

mapping_df = pd.DataFrame({
    'file名': ['file1', 'file1', 'file2', 'file2', 'file3', 'file3'],
    'カラム': ['C01', 'S02', 'C02', 'S03', 'C01', 'S02'],
    'カラム名': ['カラム名1', 'S名1', 'カラム名2', 'S名3', 'カラム名1', 'S名2']
})


# =============================================================================
# convert_logic() --- メイン変換関数
#
# 【責務】
#   1行分のロジック文字列を受け取り、変換後の文字列を返す。
#   「どのfileか」と「変換テーブル全体」を受け取って自分でフィルタする設計にすることで、
#   呼び出し側（apply）がシンプルになる。
#
# 【引数】
#   file_name  : 'file1' など。変換テーブルの絞り込みキー。
#   logic      : 'C01>0.5,S02<=0.5' などの生ロジック文字列。
#   mapping_df : 変換テーブル全体のDataFrame（file名・カラム・カラム名の3列）。
#                全体を渡してここでフィルタする設計 = 関数の外でフィルタ済みのものを
#                渡す設計より、呼び出し側の記述が簡潔になる。
# =============================================================================
def convert_logic(file_name, logic, mapping_df):

    # --- Step1: そのfileだけの変換マップを作る ---
    # mapping_df全体からfile名が一致する行だけ抽出し、
    # {カラムコード: カラム名} の辞書に変換する。
    # 例) file1なら {'C01': 'カラム名1', 'S02': 'S名1'}
    file_map = mapping_df[mapping_df['file名'] == file_name]
    col_map = dict(zip(file_map['カラム'], file_map['カラム名']))

    # --- Step2: 正規表現で「コード部分だけ」を検出して置換するクロージャを定義 ---
    # re.sub() は「マッチした箇所」を引数にした関数を置換ロジックとして受け取れる。
    # このreplace_code()はcol_map（Step1で作った辞書）をキャプチャした
    # クロージャになっており、マッチしたコードを辞書で引いて返す。
    # col_mapにないコードはそのまま返す（=未定義コードでエラーにしない安全設計）。
    def replace_code(match):
        code = match.group(0)           # マッチ全体を取得（例: 'C01'）
        return col_map.get(code, code)  # 辞書にあれば変換名、なければ元のコードをそのまま返す

    # --- Step3: ロジック文字列にre.subを適用して一括置換 ---
    # パターン [A-Z]\d+ は「大文字アルファベット1文字 + 数字1文字以上」にマッチ。
    # → C01, S02, ABC123 なども対応できる汎用パターン。
    # re.subは文字列内の全マッチ箇所にreplace_code()を順次適用してくれる。
    pattern = r'[A-Z]\d+'
    return re.sub(pattern, replace_code, logic)


# =============================================================================
# 【呼び出し側】apply() で行ごとにconvert_logicを適用
#
# DataFrame.apply(axis=1) は各行をSeries（row）として渡してくれる。
# lambda内でrow['file名']とrow['ロジック']を取り出してconvert_logicに渡す。
# mapping_dfは全行共通なので外側からそのまま渡す。
# 結果は新しい列'変換後'として格納される。
# =============================================================================
logic_df['変換後'] = logic_df.apply(
    lambda row: convert_logic(row['file名'], row['ロジック'], mapping_df),
    axis=1
)

print(logic_df[['file名', 'ロジック', '変換後']])
